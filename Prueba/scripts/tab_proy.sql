DROP TABLE IF EXISTS tab_dptos;
DROP TABLE IF EXISTS tab_regiones;

CREATE TABLE IF NOT EXISTS tab_regiones
(
    id_region           DECIMAL(2,0)    NOT NULL,
    nom_region          VARCHAR         NOT NULL,
    PRIMARY KEY(id_region)
);

CREATE TABLE IF NOT EXISTS tab_dptos
(
    id_dpto             VARCHAR         NOT NULL,
    nom_dpto            VARCHAR         NOT NULL,
    id_region           DECIMAL(5,0),
    PRIMARY KEY(id_dpto),
    FOREIGN KEY(id_region)          REFERENCES tab_regiones(id_region)
);

CREATE TABLE IF NOT EXISTS tab_municipios
(
    id_dpto             VARCHAR         NOT NULL,
    id_municipio        VARCHAR         NOT NULL,
    nom_municipio       VARCHAR         NOT NULL,
    PRIMARY KEY(id_dpto,id_municipio),
    FOREIGN KEY(id_dpto)            REFERENCES tab_dptos(id_dpto)
);

CREATE TABLE IF NOT EXISTS tab_proy
(
    id_proy             DECIMAL(8,0)    NOT NULL,
    id_bpin             VARCHAR         NOT NULL,
    nom_proy            TEXT            NOT NULL,
    val_proy            DECIMAL(18,2)   NOT NULL,
    val_obj_gral        TEXT            NOT NULL,
    id_sector           VARCHAR         NOT NULL,
    nom_pgm             VARCHAR,
    nom_estrategia      VARCHAR,
    nom_producto        VARCHAR,
    fec_registro        DATE            NOT NULL,
    fec_actualizacion   DATE            NOT NULL,
    id_region           DECIMAL(5,0),
    id_dpto             VARCHAR,
    id_municipio        VARCHAR,
    ind_xml             BOOLEAN         NOT NULL DEFAULT FALSE,
    ind_pdf             BOOLEAN         NOT NULL DEFAULT FALSE,
    ind_zip             BOOLEAN         NOT NULL DEFAULT FALSE,
    PRIMARY KEY(id_proy),
    FOREIGN KEY(id_dpto)            REFERENCES tab_dptos(id_dpto),
    FOREIGN KEY(id_dpto,id_municipio)       REFERENCES tab_municipios(id_dpto,id_municipio)
);

